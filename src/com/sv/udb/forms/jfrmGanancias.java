    private void generargalones()
    {
        List<String> dR= new ArrayList<>();
         List<String> dD= new ArrayList<>();
          List<String> dE= new ArrayList<>();
        //Cambiar color de selecciÃ³n
        this.tblTodo.setSelectionBackground(new Color(255, 255, 204));
        this.tblTodo.setSelectionForeground(Color.BLACK);
        
        DefaultTableModel model = (DefaultTableModel)this.tblTodo.getModel();
        while(model.getRowCount()>0){model.removeRow(0);} //Limpiar modelo

        //Obtenes y cargar la data CSV
        Gasolina obje = new Gasolina();
            Actual = new ArrayList<>();
            this.Actual = obje.getData();
            for(Valores temp : obje.getData())
            {
                String tipo = temp.getTipo();
                if(tipo.equals("Diesel"))
                {
                    datosD += Double.parseDouble(temp.getGalo());
                    dD.add(temp.getNombre() + " llevo: " +temp.getGalo());
                }
                if(tipo.equals("Regular"))
                {
                    datosR += Double.parseDouble(temp.getGalo());
                    dR.add(temp.getNombre() + " llevo: " +temp.getGalo());
                }
                if(tipo.equals("Especial"))
                {
                    datosE += Double.parseDouble(temp.getGalo());
                    dE.add(temp.getNombre() + " llevo: " +temp.getGalo());
                }
                
            }
            String text = String.format("<html><div style='display:table'><div><div><strong> %s</strong> "
                + "</div></div></div></html>", 
                    datosD);
            model.addRow(new Object[]{text,datosE,datosR});
    }
   
